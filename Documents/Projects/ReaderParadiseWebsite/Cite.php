        <h1>Citing and Writing</h1>

        <span>
            <h2>Why cite your sources?</h2>
            <p>You must document the sources you used in order to give recognition to the original author. Whether paraphrasing or using direct quotes, ideas that are not your own must be cited.
            </p>

            <h2>How do I choose a style?</h2>
            <p>Your instructor will provide you with the preferred style for your course. They may also have specific formatting instructions. Remain consistent with style and formatting, and provide enough information for the reader to identify the work or cited material.</p>

            <h2>About APA Style</h2>
            <p>The style manual from the American Psychological Association (APA manual) uses an author-date citation format. When citing sources in APA, regardless of the resource you are citing, you will likely need an author, date, title, source, and specific format information (such as a volume number, page numbers, a URL, publisher, etc.). The elements of the citation will depend on what you are citing (book, journal article, report, etc.) and the format (print, online).</p>

            <h2>About MLA Style</h2>
            <p>The MLA Handbook is published by the Modern Language Association, the authority on MLA documentation style. MLA style is widely used in the humanities, especially in writing on language and literature. MLA style features brief parenthetical citations in the text keyed to an alphabetical list of works cited that appears at the end of the work.</p>
            <h2>About Chicago Style</h2>
            <p>The Chicago Manual of Style (CMOS) contains two distinct citation styles or systems:</p>
            <ol>
                <li>Notes & Bibliography system for Humanities
                    Bibliography items are listed alphabetically at the end of the research paper. Notes (footnotes or endnotes) are placed in the body of the paper. 
                    Subject areas using the Notes & Bibliography system:
                    History - refer to the Notes and Bibliography tab in the Quick Guide. </li>
                <li>B. Author-Date system for Scientific/Social Sciences
                    Reference items are listed alphabetically at the end of the research paper. In-text citations are noted in the body of the paper.
                    Subject areas using the Author-Date system:
                    Anthropology, Geography & Sociology - refer to the Author-Date tab in the Quick Guide. </li>
            </ol>    


